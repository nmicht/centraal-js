/**
*Sum of two numbers and print the results in the console.
*@param {number} num1;
*param {number} num2
*/
function sum(num1, num2) {
    console.log(num1 + num2)
}
/**
*Substraction of two numbers and print the results in the console.
*@param {number} num1;
*param {number} num2
*/
function subs(num1, num2) {
    console.log(num1 - num2)
}
/**
*Multiply of two numbers and print the results in the console.
*@param {number} num1;
*param {number} num2
*/
function multiply(num1, num2) {
    console.log(num1 * num2)
}
/**
*Division of two numbers and print the results in the console.
*@param {number} num1;
*param {number} num2
*/
function division(num1, num2) {
    console.log(num1 / num2)
}
/**
*Pow of two numbers and print the results in the console.
*@param {number} num1;
*param {number} num2
*/
function square(num1, num2) {
    console.log(num1 * num2)
}
