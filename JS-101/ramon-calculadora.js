
/**
* Sum two different numbers and print the results.
* @param {number} num1
* @param {number} num2
*
*
*/

function sum(num1, num2) {
    console.log(num1 + num2);
}


/**
* Rest two different numbers and print the results.
* @param {number} num1
* @param {number} num2
*
*
*/

function rest(num1, num2) {
    console.log(num1 - num2);
}


/**
* Multiply two different numbers and print the results.
* @param {number} num1
* @param {number} num2
*
*
*/

function prod(num1, num2) {
    console.log(num1 * num2);
}

/**
* Divide two different numbers and print the results.
* @param {number} num1
* @param {number} num2
*
*
*/

function div(num1, num2) {
    console.log(num1 / num2);
}


/**
* Calculates the square of a number and print the results.
* @param {number} num
*
*/

function square(num) {
    console.log(num * num);
}
