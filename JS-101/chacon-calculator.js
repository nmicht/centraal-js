"use strict";
/**
 * Adds two numbers and print the result
 * @param  {number} num1 First number
 * @param  {number} num2 Second number
 */
function sum(num1, num2) {
    console.log(num1 + num2);
}
/**
 * Substracts two numbers and print the result
 * @param  {number} num1 First number
 * @param  {number} num2 Second number
 */
function minus(num1, num2) {
    console.log(num1 - num2);
}
/**
 * Multiplies two numbers and print the result
 * @param  {number} num1 First number
 * @param  {number} num2 Second number
 */
function prod(num1, num2) {
    console.log(num1 * num2);
}
/**
 * Divides two numbers and print the result
 * @param  {number} num1 First number
 * @param  {number} num2 Second number
 */
function div(num1, num2) {
    console.log(num1 / num2);
}

/**
 * Squares two numbers
 * @param  {string} num number to be squared.
 */
function square(num) {
    console.log(num * num);
}
